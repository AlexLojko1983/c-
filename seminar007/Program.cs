int SumDiag(int[,] array)
{
    int summDiag = 0;
    int iMin = array.GetLength(0);
    if (iMin > array.GetLength(1)) iMin = array.GetLength(1);

    for (int i = 0; i < iMin; i++)
    {
        summDiag += array[i,i];
    }
    return summDiag;
}
