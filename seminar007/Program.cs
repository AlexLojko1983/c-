int[,] FillMatrixWithRandom(int row, int column)
{
    int[,] array = new int[row, column];
    Random rnd = new Random();
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < column; j++)
        {
            array[i, j] = rnd.Next(0, 10);
        }
    }
    return array;
}

